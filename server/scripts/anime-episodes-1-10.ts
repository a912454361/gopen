// 导出场景总数统计
export const getTotalScenes = () => {
  let total = 0;
  Object.values(ANIME_EPISODES).forEach(ep => {
    total += ep.scenes.length;
  });
  return total;
};

// 获取指定范围的集数
export const getEpisodeRange = (start: number, end: number) => {
  const episodes: any[] = [];
  for (let i = start; i <= end; i++) {
    if (ANIME_EPISODES[i as keyof typeof ANIME_EPISODES]) {
      episodes.push({ episode: i, ...ANIME_EPISODES[i as keyof typeof ANIME_EPISODES] });
    }
  }
  return episodes;
};

